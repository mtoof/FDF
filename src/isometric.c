#include "fdf.h"

void	isometric(float *x, float *y, int z)
{
	float	x1;
	float	y1;

	x1 = (*x - *y) * cosf(0.866);
	*x = roundf(x1);
	y1 = (*x + *y) * sinf(0.866) - z;
	*y = roundf(y1);
}
